<h2> Inspirations to Maintain Physical Health </h2>
1.) According to World <b>Health Organization(WHO)</b>, One of the Social rules to contain Covid-19 was to maintain Social Distancing.<br>
In various Schools in <b>Africa</b>, when we want to recieve treatments or get immunized, we students are often ask to queue up on a line without
 any regards to social distancing.<br><br>

This queue up is very dangerous as it can further cause the spreading of Covid19 Virus and other Communicable/viral diseases the more.
To prevent this viral escalation, I built an online remote Medical Appointment system to allow Students to book medical Appointments online
with their Doctors thus avoiding crowds or over-crowding.<br><br>


2.)<b>Drug Prescriptions</b><br>
According to <b>FDA</b>, Drugs Prescriptions can sometimes be done based on person's <b>age and body weight</b>.<br>
<b>Source:</b> <a href="https://www.fda.gov/drugs/information-consumers-and-patients-drugs/you-age-you-and-your-medicines">
https://www.fda.gov/drugs/information-consumers-and-patients-drugs/you-age-you-and-your-medicines</a>.<br><br>

The main issues about <b>age</b> is that in some Africa(Eg. Nigeria) some students especially the dommy ones does not even know their actual age.<br>
Some Parents due to lack of education can either guess or randomly assign  a particular age to their Children.
 This becomes a problem when a students books medical appointments and doctor is trying to prescribe drugs or administer immunization 
based on the students age.  To prevent this age conflict and to ensure students gets the right drug prescription based on his/her age, then 

<b>Azure Facial Recognition AI</b> to the rescue.

 <br><br> To this Effects during Appointment Booking, The application leverages the <b>Azure Facial Recognition AI</b>
 to analyze the Patients <b>Age</b> and ensure students gets the right drug prescription based on his/her age and body weight




